package org.geekbrains;

import java.util.Arrays;

public class Calculator {
    public int Sum(int a, int b) {
        return a + b;
    }

    public int Mult(int a, int b) {
        return a * b;
    }

    public int Dividing(int a, int b) {
        return a / b;
    }

    public int[] sort(int[] arr) {
        Arrays.sort(arr);
        return arr;
    }

}

